"""
数据集划分脚本 — Stage 1
将图片和标注按 70/20/10 划分为 train/val/test 三个子集。

用法 (PC 端):
    python split_dataset.py \
        --input_dir ./raw_dataset/ \
        --output_dir ./dataset_study_yolo/ \
        --train 0.7 --val 0.2 --test 0.1

要求:
    raw_dataset/ 下需要有 images/ 和 labels/ 两个子目录，
    其中图片和标注文件一一对应（同名不同后缀）。

    例如:
        raw_dataset/
        ├── images/
        │   ├── img001.jpg
        │   ├── img002.jpg
        │   └── ...
        └── labels/
            ├── img001.txt
            ├── img002.txt
            └── ...
"""

import os
import sys
import random
import shutil
import argparse
from pathlib import Path


def get_paired_files(images_dir, labels_dir, img_exts=None):
    """获取图片和标注配对的文件列表。"""
    if img_exts is None:
        img_exts = {'.jpg', '.jpeg', '.png', '.bmp', '.tif', '.tiff'}

    pairs = []
    for img_file in sorted(os.listdir(images_dir)):
        img_path = os.path.join(images_dir, img_file)
        if not os.path.isfile(img_path):
            continue
        stem, ext = os.path.splitext(img_file)
        if ext.lower() not in img_exts:
            continue

        # 查找对应的标注文件
        label_file = stem + '.txt'
        label_path = os.path.join(labels_dir, label_file)
        if os.path.isfile(label_path):
            pairs.append((img_file, label_file))
        else:
            print(f"[WARN] 图片 {img_file} 缺少标注文件，跳过")

    return pairs


def split_and_copy(pairs, input_dirs, output_dir, train_r, val_r, test_r):
    """按比例划分并复制文件到输出目录。"""
    total = train_r + val_r + test_r
    train_pct = train_r / total
    val_pct = val_r / total

    # 随机打乱
    random.shuffle(pairs)
    n = len(pairs)

    train_end = int(n * train_pct)
    val_end = int(n * (train_pct + val_pct))

    splits = {
        'train': pairs[:train_end],
        'val': pairs[train_end:val_end],
        'test': pairs[val_end:],
    }

    src_images = input_dirs['images']
    src_labels = input_dirs['labels']

    for split_name, split_pairs in splits.items():
        out_img_dir = os.path.join(output_dir, 'images', split_name)
        out_lbl_dir = os.path.join(output_dir, 'labels', split_name)
        os.makedirs(out_img_dir, exist_ok=True)
        os.makedirs(out_lbl_dir, exist_ok=True)

        for img_file, lbl_file in split_pairs:
            shutil.copy2(
                os.path.join(src_images, img_file),
                os.path.join(out_img_dir, img_file)
            )
            shutil.copy2(
                os.path.join(src_labels, lbl_file),
                os.path.join(out_lbl_dir, lbl_file)
            )

        print(f"[{split_name}] {len(split_pairs)} 张图片")

    return splits


def main():
    parser = argparse.ArgumentParser(description="YOLO 数据集划分工具")
    parser.add_argument('--input_dir', required=True,
                        help='原始数据集目录 (包含 images/ 和 labels/)')
    parser.add_argument('--output_dir', default='./dataset_study_yolo',
                        help='输出目录')
    parser.add_argument('--train', type=float, default=0.7,
                        help='训练集比例 (默认 0.7)')
    parser.add_argument('--val', type=float, default=0.2,
                        help='验证集比例 (默认 0.2)')
    parser.add_argument('--test', type=float, default=0.1,
                        help='测试集比例 (默认 0.1)')
    parser.add_argument('--seed', type=int, default=42,
                        help='随机种子 (默认 42)')
    args = parser.parse_args()

    random.seed(args.seed)

    input_images = os.path.join(args.input_dir, 'images')
    input_labels = os.path.join(args.input_dir, 'labels')

    if not os.path.isdir(input_images):
        print(f"[ERROR] 图片目录不存在: {input_images}")
        sys.exit(1)
    if not os.path.isdir(input_labels):
        print(f"[ERROR] 标注目录不存在: {input_labels}")
        sys.exit(1)

    print(f"输入目录: {args.input_dir}")
    print(f"输出目录: {args.output_dir}")
    print(f"划分比例: train={args.train} val={args.val} test={args.test}")

    # 获取配对文件
    pairs = get_paired_files(input_images, input_labels)
    print(f"找到 {len(pairs)} 对图片-标注文件")

    if len(pairs) < 10:
        print("[ERROR] 数据量太少，至少需要 10 对以上")
        sys.exit(1)

    # 划分并复制
    splits = split_and_copy(
        pairs,
        {'images': input_images, 'labels': input_labels},
        args.output_dir,
        args.train, args.val, args.test
    )

    # 输出 dataset.yaml 到输出目录
    yaml_content = f"""# Auto-generated by split_dataset.py
path: .
train: images/train
val: images/val
test: images/test
nc: 4
names:
  0: person
  1: phone
  2: book
  3: laptop
"""
    yaml_path = os.path.join(args.output_dir, 'dataset.yaml')
    with open(yaml_path, 'w', encoding='utf-8') as f:
        f.write(yaml_content)
    print(f"dataset.yaml 已写入: {yaml_path}")

    print("\n划分完成!")


if __name__ == '__main__':
    main()
